uint8_t photon_unicode_to_cp437(uint32_t cp)
{
    /* ASCII 0x20-0x7E map directly */
    if (cp >= 0x20 && cp <= 0x7E) return (uint8_t)cp;
    /* Binary search */
    int lo = 0, hi = REVERSE_MAP_LEN - 1;
    while (lo <= hi) {
        int mid = (lo + hi) / 2;
        if (reverse_map[mid].unicode == cp) return reverse_map[mid].cp437;
        if (reverse_map[mid].unicode < cp) lo = mid + 1;
        else hi = mid - 1;
    }
    return 0x3F; /* ? */
}
